type Key = keyof typeof dict["en"];

interface LangStore {
  lang: Lang;
  setLang: (l: Lang) => void;
}

export const useLang = create<LangStore>()(
  persist((set) => ({ lang: "en", setLang: (l) => set({ lang: l }) }), { name: "nyayai-lang" })
);

export function useT() {
  const { lang } = useLang();
  return (k: Key): string => (dict[lang] as any)[k] ?? (dict.en as any)[k] ?? k;
}
